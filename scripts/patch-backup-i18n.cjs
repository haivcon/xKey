function escapeValue(value) {
  return value.replace(/\\/g, '\\\\').replace(/"/g, '\\"');
}

function objectBlock(obj) {
  return `{\n${Object.entries(obj).map(([key, value]) => `    "${key}": "${escapeValue(value)}"`).join(',\n')}\n  }`;
}

for (const [file, locale] of Object.entries(translations)) {
  const filePath = path.join('src', 'locales', file);
  let source = fs.readFileSync(filePath, 'utf8');
  const nextBlock = `"backupExportChallenge": ${objectBlock(locale.c)},\n  "backupImportSelection": ${objectBlock(locale.s)}`;
  const nextSource = source.replace(
    /"backupExportChallenge": \{[\s\S]*?\r?\n  \},\r?\n  "backupImportSelection": \{[\s\S]*?\r?\n  \}/,
    nextBlock,
  );

  if (nextSource === source) {
    throw new Error(`Could not patch ${file}`);
  }

  fs.writeFileSync(filePath, nextSource);
}

