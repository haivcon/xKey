const insertAfterKey = '    "applyScale":';

for (const [lang, values] of Object.entries(translations)) {
  const file = join('src', 'locales', `${lang}.ts`);
  let text = readFileSync(file, 'utf8');

  const settingsStart = text.indexOf(settingsKey);
  if (settingsStart === -1) throw new Error(`Missing settings block in ${file}`);

  const rest = text.slice(settingsStart);
  const nextBlock = rest.search(/\n  "[^"]+": \{/);
  const settingsEnd = nextBlock === -1 ? text.length : settingsStart + nextBlock;
  const before = text.slice(0, settingsStart);
  let settingsBlock = text.slice(settingsStart, settingsEnd);
  const after = text.slice(settingsEnd);

  settingsBlock = settingsBlock.replace(/\n    "(dpiMode|dpiModeDesc|deviceDpi|targetDpi|effectiveScale|targetDpiCustom|dpiCalculatedScale|dpiModeConfirmTitle|dpiModeEnableConfirmMessage|dpiModeDisableConfirmMessage|enableDpiMode|disableDpiMode|dpiConfirmTitle|dpiConfirmMessage|applyDpi)": ".*?",/g, '');

  const applyLineMatch = settingsBlock.match(/^    "applyScale": ".*?",$/m);
  if (!applyLineMatch) throw new Error(`Missing applyScale in settings block for ${file}`);

  const additions = Object.entries(values)
    .map(([key, value]) => `    "${key}": ${JSON.stringify(value)},`)
    .join('\n');

  settingsBlock = settingsBlock.replace(applyLineMatch[0], `${applyLineMatch[0]}\n${additions}`);
  text = before + settingsBlock + after;
  writeFileSync(file, text, 'utf8');
}

